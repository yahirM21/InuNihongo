            <p class="justificado">Los diptongos son utilizados en el japonés al igual que en el español, un diptongo es la mezcla de dos
               vocales una después de la otra que al pronunciarlas se genera un solo sonido, como en las palabras “Bien” o “Puerta”. Sin
                embargo en el japonés se utilizan las sílabas con el sonido “<font color="red">i</font>” mas los sonidos “<font color="red">ya</font>”,
                 “<font color="red">yu</font>” y “<font color="red">yo</font>” para crearlos, por ejemplo el sonido de un gato “<font color="red">nya</font>” que se escribe con 
                 la letra “<font color="red">mi</font>” + “<font color="red">ya</font>”.Cabe recalcar que las letras “<font color="red">ya</font>”, 
                 “<font color="red">yu</font>” y “<font color="red">yo</font>” se escriben más chiquitas de lo normal ya que de lo contrario usando el
                  ejemplo anterior estarias escribiendo “<font color="red">miya</font>”.</p> 
